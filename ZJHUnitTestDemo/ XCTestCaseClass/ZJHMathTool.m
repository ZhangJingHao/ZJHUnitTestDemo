#import "ZJHMathTool.h"

@implementation ZJHMathTool

// a+b
- (int)sumA:(int)a andB:(int)b {
    return a + b;
}

// a-b
- (int)subA:(int)a andB:(int)b {
    return a - b;
}

// axb
- (int)multiplyA:(int)a andB:(int)b {
    return a * b;
}

// a√∑b
- (int)divideA:(int)a andB:(int)b {
    return a / b;
}

@end
